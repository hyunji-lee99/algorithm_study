# 점화식 규칙을 찾아서 푸는 DP 문제
# n=1 : 1 => 1
# n=2 : 10 => 1
# n=3 : 101,100 => 2
# n=4 : 1010, 1001, 1000 => 3
# n=5: 10100, 10101, 10010, 10001, 10000 => 5
# n=6 : 101000, 100100, 100010, 100001, 100000, 101010, 101001, 100101 =>8
import sys

n=int(sys.stdin.readline())
dp=[0]*(n+1)
dp[1]=1
if n>1:
    dp[2]=1
for i in range(3, n+1):
    dp[i]=dp[i-2]+dp[i-1]

print(dp[n])